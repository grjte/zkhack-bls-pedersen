fn main() {
    welcome();
    puzzle(PUZZLE_DESCRIPTION);
    let (pk, ms, sigs) = puzzle_data();

    // prepare my message
    // b"" creates a byte string
    let msg = b"grjte";

    // write my message and the leaked messages to files for calculation in sage
    create_sage_inputs(msg, ms);

    // DO CALCULATION IN SAGE

    // get the results of the sage calculation (the vector we'll use to multiply the signature)
    let crack_vector = get_sage_output();

    // use the vector from sage to calculate the signature
    let sig = forge_signature(crack_vector, sigs);

    // verify
    verify(pk, msg, sig);

    // output signature in hex
    let mut sig_vec = Vec::new();
    sig.serialize(&mut sig_vec).unwrap();
    let sig_hex = hex::encode(sig_vec);
    println!("sig: {}", sig_hex);
}

fn create_sage_inputs(msg: &[u8], ms: Vec<Vec<u8>>) {
    // 1. represent message as a 256x1 bit vector & save it to file
    write_vec_to_file(msg);

    // 2. hash the blake2s messages & print them to a file
    write_matrix_to_file(ms);
}

fn bytes_to_bits_vector(b2hash: &[u8]) -> String {
    let msg_bits = bytes_to_bits(b2hash);
    let mut msg_out: String = String::with_capacity(msg_bits.len());
    let mut iter = 0;
    msg_out.push('[');
    for bit in msg_bits {
        if bit {
            msg_out.push('1');
        } else {
            msg_out.push('0');
        }
        if iter < 255 {
            msg_out.push(',');
        }
        iter += 1;
    }
    msg_out.push(']');
    msg_out
}

fn write_vec_to_file(msg: &[u8]) {
    let msg_b2hash = hash_to_curve(msg).0;
    // let msg_b2hash = blake2s_simd::blake2s(msg);
    let mut file = File::create(format!("vector.sage")).unwrap();
    let msg_out = bytes_to_bits_vector(&msg_b2hash);
    file.write_all(b"v=vector(").unwrap();
    file.write_all(msg_out.as_ref()).unwrap();
    file.write_all(b")").unwrap();
}

fn write_matrix_to_file(ms: Vec<Vec<u8>>) {
    let mut file = File::create(format!("matrix.sage")).unwrap();
    file.write_all(b"a=matrix([").unwrap();
    let mut iter = 0;
    for m in ms.iter() {
        // get the hash
        let m_b2hash = hash_to_curve(m).0;
        // let m_b2hash = blake2s_simd::blake2s(m);
        // turn it into a string of bits
        let m_str = bytes_to_bits_vector(&m_b2hash);

        // write hash to file
        file.write_all(m_str.as_ref()).unwrap();
        if iter < 255 {
            file.write_all(b",\n").unwrap();
        }
        iter += 1;
    }
    file.write_all(b"])").unwrap();
}

fn forge_signature(g_mults: [Fr; 256], sigs: Vec<G1Affine>) -> G1Affine {
    // Do arithmetic using ProjectiveCurve representation
    let mut sum = G1Projective::zero();

    for (i, g_multiplier) in g_mults.iter().enumerate() {
        // convert sigs rom Affine to Projective for arithmetic
        let additive = sigs[i].into_projective().mul(g_multiplier);
        sum += additive;
    }

    // convert to AffineCurve representation
    let affine = G1Affine::from(sum);

    // return our forged signature
    affine
}

